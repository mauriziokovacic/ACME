function [P] = color_palette(Name)
% COLOR_PALETTE  Returns a color palette
%   P = COLOR_PALETTE() returns a cell array with all the color palettes
%   from ACME
%   P = COLOR_PALETTE(Name) returns the color palette specified by Name,
%   either being a built-in Matlab one or from ACME.
if( nargin < 1 )
    Name = [];
end
persistent PData;
PData = {...
    'black',  [0 0 0; 1 1 1];...
    'r',      [1 0 0; 1 1 1];...
    'g',      [0 1 0; 1 1 1];...
    'b',      [0 0 1; 1 1 1];...
    'c',      [0 1 1; 1 1 1];...
    'm',      [1 0 1; 1 1 1];...
    'y',      [1 1 0; 1 1 1];...
    'fire',   flipud(color2double([255 255 255; 239 228 176; 255 242 0; 255 201 14; 255 127 39; 237 28 36; 136 0 21]));...
    'brown',  hex2rgb(['#7f4d3e';'#b87c4c';'#e2b659';'#f9f871']);...
    'orange', flipud(hex2rgb(['#ffffd4';'#fed98e';'#fe9929';'#d95f0e';'#993404']));...
    'blue',   rot90(hex2rgb(['#ffffd4';'#fed98e';'#fe9929';'#d95f0e';'#993404']),2);...
    'green',  flipud(hex2rgb(['#ffffcc';'#c2e699';'#78c679';'#31a354';'#006837']));...
    'mint',   flipud(hex2rgb(['#a4fcce';'#66c456';'#318e49';'#184932';'#0a382b';]));...
    'purple', flipud(hex2rgb(['#edf8fb';'#b3cde3';'#8c96c6';'#8856a7';'#810f7c']));...
    'sign',   flipud(hex2rgb(['#ca0020';'#f4a582';'#f7f7f7';'#92c5de';'#0571b0']));...
    'king',   hex2rgb(['#1a2a6c';'#b21f1f';'#fdbb2d']);...
    'vision', hex2rgb(['#000046';'#1cb5e0']);...
    'turbo',  flipud(hex2rgb(['#f1f2b5';'#135058']));...
    'pinot',  flipud(hex2rgb(['#4b6cb7';'#182848']));...
    'sky',    hex2rgb(['#076585';'#ffffff']);...
    'aqua',   hex2rgb(['#13547a';'#80d0c7']);...
    'dusk',   flipud(hex2rgb(['#ffd89b';'#19547b']));...
    'relay',  hex2rgb(['#3a1c71';'#d76d77';'#ffaf7b']);...
    'sweet',  hex2rgb(['#3f51b1';'#5a55ae';'#7b5fac';'#8f6aae';'#a86aa4';'#cc6b8e';'#f18271';'#f3a469';'#f7c978']);...
    'phoenix',hex2rgb(['#f83600';'#f9d423']);...     
    'RYB',    hex2rgb(['#FE2712';'#FC600A';'#FB9902';'#FCCC1A';'#FEFE33';'#B2D732';'#66B032';'#347C98';'#0247FE';'#4424D6';'#8601AF';'#C21460']);...
    'scale',  hex2rgb(['#000000';'#7f00ff';'#0000ff';'#00ffff';'#00ff00';'#ffff00';'#ff8000';'#ff0000']);...
    'paint',  [Red();Yellow();Green();Cyan();Blue();Magenta()];...
    'pastel', flipud(hex2rgb(['#ffb3ba';'#ffdfba';'#ffffba';'#baffc9';'#bae1ff']));...
    'cinolib',[RedPastel();OrangePastel();YellowPastel();GreenPastel();CyanPastel();PinkPastel();VioletPastel();MagentaPastel()];...
    'matlab', [MatlabBlue();MatlabOrange();MatlabYellow();MatlabPurple();MatlabGreen();MatlabCyan();MatlabRed()];...
         };
     
if( isempty(Name) )
    P = PData;
else
    i = [];
    if( ischar(Name) || isstring(Name) )
        i = find( strcmpi(PData(:,1),Name) );
    else
        if( Name > 0 && Name <= row(PData) )
            i = Name;
        end
    end
    P = [];
    if( ~isempty(i) )
        P = cell2mat(PData(i,2));
    end
end
end