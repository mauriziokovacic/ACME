def palette(name,device='cuda:0'):
    """
    Returns the color palette with the specified name

    Parameters
    ----------
    name : str
        the name of the color palette
    device : str or torch.device (optional)
        the device the tensor will be stored to (default is 'cuda:0')

    Returns
    -------
    Tensor
        the color tensor
    """

    color = {
                'black'   : _black,
                'r'       : _r,
                'g'       : _g,
                'b'       : _b,
                'c'       : _c,
                'm'       : _m,
                'y'       : _y,
                'fire'    : _fire,
                'brown'   : _brown,
                'orange'  : _orange,
                'blue'    : _blue,
                'green'   : _green,
                'mint'    : _mint,
                'purple'  : _purple,
                'sign'    : _sign,
                'king'    : _king,
                'vision'  : _vision,
                'turbo'   : _turbo,
                'pinot'   : _pinot,
                'sky'     : _sky,
                'aqua'    : _aqua,
                'dusk'    : _dusk,
                'relay'   : _relay,
                'sweet'   : _sweet,
                'phoenix' : _phoenix,
                'RYB'     : _RYB,
                'scale'   : _scale,
                'paint'   : _paint,
                'pastel'  : _pastel,
                'cinolib' : _cinolib,
                'matlab'  : _matlab,
            }
    if name in color:
        return color[name]().to(device=device)
    assert False, 'Unknown color palette'
